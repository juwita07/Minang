package com.kamusminang;

import android.graphics.Bitmap;

public class Kamus {

	private String indonesia;
	private String minang;
	private String keterangan;
	private Bitmap imageAksara;

	public String getIndonesia() {
		return indonesia;
	}

	public void setIndonesia(String indonesia) {
		this.indonesia = indonesia;
	}

	public String getMinang() {
		return minang;
	}

	public void setMinang(String minang) {
		this.minang = minang;
	}

	public String getKeterangan() {
		return keterangan;
	}

	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}

	public Bitmap getImageAksara() {
		return imageAksara;
	}

	public void setImageAksara(Bitmap imageAksara) {
		this.imageAksara = imageAksara;
	}

}
